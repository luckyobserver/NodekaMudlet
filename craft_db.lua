Craft.scraps = { }
for _index_0 = 1, #components do
  local item = components[_index_0]
  item.bag = { }
  item.count = 0
  local _exp_0 = item.material
  if "leather" == _exp_0 then
    item.cost = 3
  elseif "wood" == _exp_0 or "cloth" == _exp_0 then
    item.cost = 4
  elseif "metal" == _exp_0 then
    item.cost = 5
  end
  Craft.scraps[item.name] = table.deepcopy(item)
  item.value = item.cost * item.tier
  item.expertise = false
  item.artistry = false
  Craft.component[item.name] = item
end
